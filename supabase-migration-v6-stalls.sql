-- =====================================================
-- Migration V6: Stall inventory + allocation (booth map)
-- Run in Supabase SQL Editor (after v5). ADDITIVE ONLY.
-- Canonical 314-stall inventory drives the 2D map, the live
-- per-type slot counter, and the over-confirm guard.
-- Source of truth: public/stalls.json (from the official SDP2026 plan).
-- =====================================================

CREATE TABLE IF NOT EXISTS stalls (
  code TEXT PRIMARY KEY,                       -- exact code: FT01, FS1, TS6, BS13
  type TEXT NOT NULL CHECK (type IN ('FT','FS','TS','BS')),
  num INTEGER NOT NULL,
  col INTEGER, "row" INTEGER, w INTEGER, h INTEGER,   -- grid geometry from the plan
  status TEXT NOT NULL DEFAULT 'available'
    CHECK (status IN ('available','held','allocated','blocked')),
  application_id UUID REFERENCES vendor_applications(id) ON DELETE SET NULL,
  vendor_profile_id UUID REFERENCES vendor_profiles(id) ON DELETE SET NULL,
  allocated_at TIMESTAMPTZ,
  allocated_by TEXT,
  notes TEXT,
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE stalls ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Service role manages stalls" ON stalls;
CREATE POLICY "Service role manages stalls" ON stalls FOR ALL USING (true) WITH CHECK (true);

CREATE INDEX IF NOT EXISTS idx_stalls_type_status ON stalls(type, status);
CREATE INDEX IF NOT EXISTS idx_stalls_application ON stalls(application_id);

-- one application can't accidentally double-hold the same stall; a stall has one occupant
CREATE UNIQUE INDEX IF NOT EXISTS uniq_stall_profile
  ON stalls(vendor_profile_id) WHERE vendor_profile_id IS NOT NULL;

-- Live availability per type (powers the slot counter + over-confirm guard)
CREATE OR REPLACE VIEW stall_availability AS
SELECT type,
  COUNT(*)                                        AS total,
  COUNT(*) FILTER (WHERE status = 'available')    AS available,
  COUNT(*) FILTER (WHERE status = 'held')         AS held,
  COUNT(*) FILTER (WHERE status = 'allocated')    AS allocated,
  COUNT(*) FILTER (WHERE status = 'blocked')      AS blocked
FROM stalls GROUP BY type;

